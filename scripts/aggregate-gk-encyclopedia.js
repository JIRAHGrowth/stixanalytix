/**
 * Aggregate per-video technique extractions into prompts/gk_techniques.md.
 *
 * Reads all JSONs in prompts/gk_techniques/raw/, groups by canonical name,
 * merges aliases / cues / indicators across sources, and writes a single
 * human-readable markdown file the live pipeline can include in Gemini prompts.
 *
 * Idempotent: regenerates the markdown each run from the raw JSONs (the JSONs
 * are the source of truth; the markdown is derived).
 *
 * Manual cleanup pass is expected after first generation — the merge is
 * conservative and may produce two entries for the same technique under
 * different names. Edit the raw JSONs (canonicalise names) and re-run.
 *
 * Usage:
 *   node scripts/aggregate-gk-encyclopedia.js
 */

const fs = require('fs');
const path = require('path');

function normaliseName(s) {
  return String(s || '').toLowerCase().trim().replace(/\s+/g, ' ').replace(/[._-]+/g, ' ');
}

function uniq(arr) {
  const seen = new Set();
  return arr.filter(x => {
    const k = typeof x === 'string' ? x.toLowerCase().trim() : JSON.stringify(x);
    if (seen.has(k)) return false;
    seen.add(k);
    return true;
  });
}

function main() {
  const rawDir = path.join(__dirname, '..', 'prompts', 'gk_techniques', 'raw');
  const outPath = path.join(__dirname, '..', 'prompts', 'gk_techniques.md');

  if (!fs.existsSync(rawDir)) {
    console.error(`No raw dir at ${rawDir}. Run build-gk-encyclopedia.js first.`);
    process.exit(1);
  }

  const files = fs.readdirSync(rawDir).filter(f => f.endsWith('.json')).sort();
  console.log(`Reading ${files.length} extraction(s) from ${rawDir}`);

  // Group by normalised name. First seen entry wins on `name` casing.
  const techniques = new Map();
  let totalEntries = 0;
  let skipped = 0;
  const sources = [];

  for (const f of files) {
    const j = JSON.parse(fs.readFileSync(path.join(rawDir, f), 'utf8'));
    if (j._parse_error) {
      console.warn(`  skip ${f} — parse error: ${j._parse_error}`);
      skipped++;
      continue;
    }
    sources.push({ file: f, count: (j.techniques || []).length, notes: j.notes || null });
    for (const t of j.techniques || []) {
      totalEntries++;
      const key = normaliseName(t.name);
      if (!key) continue;
      if (!techniques.has(key)) techniques.set(key, {
        name: t.name,
        aliases: [],
        gemini_default_terms: [],
        purposes: [],
        visual_indicators: [],
        coach_cues: [],
        distinguishes_from: [],
        common_mistakes: [],
        sources: [],
      });
      const e = techniques.get(key);
      e.aliases.push(...(t.aliases || []));
      e.gemini_default_terms.push(...(t.gemini_default_terms || []));
      if (t.purpose) e.purposes.push(t.purpose);
      e.visual_indicators.push(...(t.visual_indicators || []));
      e.coach_cues.push(...(t.coach_cues || []));
      e.distinguishes_from.push(...(t.distinguishes_from || []));
      e.common_mistakes.push(...(t.common_mistakes || []));
      if (t.source) e.sources.push(t.source);
    }
  }

  // De-duplicate
  for (const e of techniques.values()) {
    e.aliases = uniq(e.aliases);
    e.gemini_default_terms = uniq(e.gemini_default_terms);
    e.purposes = uniq(e.purposes);
    e.visual_indicators = uniq(e.visual_indicators);
    e.coach_cues = uniq(e.coach_cues);
    e.distinguishes_from = uniq(e.distinguishes_from);
    e.common_mistakes = uniq(e.common_mistakes);
  }

  // Sort by name
  const sorted = [...techniques.values()].sort((a, b) => a.name.localeCompare(b.name));

  // Render markdown
  const lines = [];
  lines.push('# STIX Goalkeeper Technique Encyclopedia');
  lines.push('');
  lines.push(`Generated from ${files.length - skipped} reference video(s) by [scripts/aggregate-gk-encyclopedia.js](../scripts/aggregate-gk-encyclopedia.js). Do not edit this file by hand — edit the source JSONs in [prompts/gk_techniques/raw/](gk_techniques/raw/) and re-run the aggregator.`);
  lines.push('');
  lines.push('This file is **reference material the live Gemini pipeline reads at runtime**. When analysing a match, the pipeline includes selected sections of this file as calibration context so the model uses the same technique names and visual indicators a STIX-trained coach would.');
  lines.push('');
  lines.push(`**Last generated:** ${new Date().toISOString()} from ${totalEntries} raw entries → ${sorted.length} canonical techniques.`);
  lines.push('');
  lines.push('---');
  lines.push('');
  lines.push('## Techniques');
  lines.push('');

  for (const e of sorted) {
    lines.push(`### ${e.name}`);
    lines.push('');
    if (e.aliases.length) {
      lines.push(`**Aliases (coach vocabulary):** ${e.aliases.join(', ')}`);
      lines.push('');
    }
    if (e.gemini_default_terms.length) {
      lines.push(`**Likely Gemini-default terms (translate from these):** ${e.gemini_default_terms.join(', ')}`);
      lines.push('');
    }
    if (e.purposes.length) {
      lines.push(`**Purpose:** ${e.purposes.join(' / ')}`);
      lines.push('');
    }
    if (e.visual_indicators.length) {
      lines.push(`**Visual indicators (camera-observable):**`);
      e.visual_indicators.forEach(v => lines.push(`- ${v}`));
      lines.push('');
    }
    if (e.coach_cues.length) {
      lines.push(`**Coach cues (verbatim):**`);
      e.coach_cues.forEach(c => {
        if (typeof c === 'string') lines.push(`- "${c}"`);
        else lines.push(`- [${c.timestamp || '?'}] "${c.quote || ''}"`);
      });
      lines.push('');
    }
    if (e.distinguishes_from.length) {
      lines.push(`**Distinguishes from:**`);
      e.distinguishes_from.forEach(d => {
        if (typeof d === 'string') lines.push(`- ${d}`);
        else lines.push(`- **${d.technique}** — ${d.differentiator}`);
      });
      lines.push('');
    }
    if (e.common_mistakes.length) {
      lines.push(`**Common mistakes (per coach):**`);
      e.common_mistakes.forEach(m => lines.push(`- ${m}`));
      lines.push('');
    }
    if (e.sources.length) {
      const fileNames = uniq(e.sources.map(s => s.video_filename).filter(Boolean));
      const coaches = uniq(e.sources.map(s => s.coach_name).filter(Boolean));
      lines.push(`**Sources:** ${fileNames.join(', ') || 'unknown'}${coaches.length ? ` (${coaches.join(', ')})` : ''}`);
      lines.push('');
    }
    lines.push('---');
    lines.push('');
  }

  // Source manifest
  lines.push('## Source manifest');
  lines.push('');
  lines.push('| File | Techniques extracted | Notes |');
  lines.push('|---|---|---|');
  for (const s of sources) {
    lines.push(`| ${s.file} | ${s.count} | ${s.notes || ''} |`);
  }
  if (skipped) {
    lines.push('');
    lines.push(`(${skipped} extraction file(s) skipped due to parse errors — investigate prompts/gk_techniques/raw/ for _parse_error.)`);
  }

  fs.writeFileSync(outPath, lines.join('\n'));
  console.log(`Wrote ${path.relative(process.cwd(), outPath)} — ${sorted.length} technique(s) from ${totalEntries} raw entries.`);
}

main();
